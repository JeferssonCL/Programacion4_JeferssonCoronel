package practica7_GestionDeTicketsBancarios.Parte1;

/**
 * A utility class that provides ANSI escape codes for coloring text.
 * <p>
 * This class defines constants for common ANSI escape codes used to
 * color text in console output. These constants can be used to easily
 * add color to printed text.
 */
public class ManagerColor {
    public static final String RESET = "\u001B[0m";
    public static final String YELLOW = "\u001B[33m";
    public static final String CYAN = "\u001B[36m";
}